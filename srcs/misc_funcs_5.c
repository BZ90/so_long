#include "so_long.h"

int	move(int *pos, int i, int j)
{
	pos[0] = i;
	pos[1] = j;
	return (0);
}

int	p_to_p_len(char **p_to_p)
{
	int	i;

	i = 0;
	while (p_to_p[i] != NULL)
		++i;
	return (i);
}
